CREATE TABLE
    CP2_MSG_PAGE_MAP
    (
        URL VARCHAR2(256),
        NAME VARCHAR2(256),
        PKG_NO VARCHAR2(20),
        STATUS CHAR(2),
        COM_STATUS CHAR(2),
        INCLUDED CHAR(4),
        EXCLUDED CHAR(4),
        DRCT CHAR(1),
        BIZCTGYCODE CHAR(5),
        RSV1 VARCHAR2(256),
        RSV2 VARCHAR2(256),
        RSV3 VARCHAR2(256),
        RSV4 VARCHAR2(256),
        RSV5 VARCHAR2(256)
    );
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.URL
IS
    '页面URL';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.NAME
IS
    '页面名称';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.PKG_NO
IS
    '报文类型';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.STATUS
IS
    '报文业务状态';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.COM_STATUS
IS
    '报文通讯状态';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.INCLUDED
IS
    '包含业务类型';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.EXCLUDED
IS
    '不包含业务类型';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.DRCT
IS
    '报文方向';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.BIZCTGYCODE
IS
    '业务种类';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.RSV1
IS
    '备用字段1';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.RSV2
IS
    '备用字段2';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.RSV3
IS
    '备用字段3';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.RSV4
IS
    '备用字段4';
COMMENT ON COLUMN CP2_MSG_PAGE_MAP.RSV5
IS
    '备用字段5';
    '备用字段5';